package PildoraExercises.oop.car_default;

public class Car {

    //attributes
    int wheels;
    int height;
    int width;
    int length;
    int weight;
    int engine;

    //constructor
    public Car(int wheels, int height, int width, int length, int weight, int engine) {
        this.wheels = wheels;
        this.height = height;
        this.width = width;
        this.length = length;
        this.weight = weight;
        this.engine = engine;
    }
}
