package java_1;

public class Variables {
    public static void main(String[] args) {
        //declare variables
        int id;
        boolean isLoggedIn;
        double bankAccountBalance;
        String question;
        int answer;

        //initialize variables
        id = 123;
        isLoggedIn = false;
        bankAccountBalance = 123.45;
        question = "This is a a simple question, how many years do you have as a bank customer?";
        answer = 12;

        //print in console
        System.out.println("id = " + id);
        System.out.println("isLoggedIn = " + isLoggedIn);
        System.out.println("bankAccountBalance = " + bankAccountBalance);
        System.out.println("question = " + question);
        System.out.println("answer = " + answer);

    }
}
